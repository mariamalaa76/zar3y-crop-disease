def benchmark_tflite(model_path, image_paths, true_labels):
    # Initialize the engine backend using a robust configuration strategy
    try:
        # Attempt to initialize without special delegates first
        interp = tf.lite.Interpreter(model_path=str(model_path))
        interp.allocate_tensors()
    except RuntimeError as e:
        print(f"   ⚠️  Note: XNNPACK runtime error encountered for {model_path.name}. Retrying with experimental_delegates=[].")
        # If XNNPACK fails, try again with explicit delegate disabling
        interp = tf.lite.Interpreter(
            model_path=str(model_path),
            experimental_delegates=[]
        )
        interp.allocate_tensors()

    input_details = interp.get_input_details()[0]
    output_details = interp.get_output_details()[0]

    inp_idx = input_details["index"]
    out_idx = output_details["index"]

    latencies, preds = [], []
    for path in image_paths:
        img = np.expand_dims(preprocess(path), axis=0).astype(np.float32)

        # Scale inputs down to appropriate integer constraints if required
        if input_details["dtype"] == np.int8:
            scale, zero_point = input_details["quantization"]
            img = (img / scale + zero_point).astype(np.int8)

        interp.set_tensor(inp_idx, img)

        t0 = time.perf_counter()
        interp.invoke()
        latencies.append(time.perf_counter() - t0)

        out = interp.get_tensor(out_idx)

        # De-quantize outputs back into probability space before decoding
        if output_details["dtype"] == np.int8:
            out_scale, out_zero_point = output_details["quantization"]
            out = (out.astype(np.float32) - out_zero_point) * out_scale

        preds.append(int(np.argmax(out)))

    correct = sum(p == l for p, l in zip(preds, true_labels))
    return {
        "avg_latency_ms": round(float(np.mean(latencies)) * 1000, 2),
        "p95_latency_ms": round(float(np.percentile(latencies, 95)) * 1000, 2),
        "accuracy":       round(correct / len(true_labels), 4),
    }

# =====================================================================
