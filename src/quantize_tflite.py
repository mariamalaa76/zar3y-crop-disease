def load_split_paths(split_name: str):
    """Read paths + labels written by data_prep.py (one path per line)."""
    path_file  = SPLIT_DIR / f"{split_name}_paths.txt"
    label_file = SPLIT_DIR / f"{split_name}_labels.txt"

    if not path_file.exists():
        # fallback: walk the directory tree directly
        print(f"  Split file not found — scanning {DATA_DIR} directly …")
        paths, labels = [], []
        for idx, cls in enumerate(CLASSES):
            cls_dir = DATA_DIR / cls
            if not cls_dir.exists():
                continue
            for img_path in sorted(cls_dir.glob("*.jpg"))[:200]:
                paths.append(img_path)
                labels.append(idx)
        return paths, labels

    paths  = [Path(p.strip()) for p in path_file.read_text().splitlines()]
    labels = [int(l.strip()) for l in label_file.read_text().splitlines()]
    return paths, labels

