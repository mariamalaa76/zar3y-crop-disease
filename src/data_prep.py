def split_data(SOURCE, TRAINING, VALIDATION, TESTING, SPLIT_SIZE_TRN):
    files = []
    imgs_list = [filename for filename in os.listdir(SOURCE)]
    print(f"{SOURCE} size: {len(imgs_list)}")
    for filename in imgs_list:
        file = os.path.join(SOURCE,filename)
        if os.path.getsize(file) > 0:
            files.append(filename)
        else:
            print(filename + " is zero length, so ignoring.")

    training_length = int(len(files) * SPLIT_SIZE_TRN)
    validation_length = testing_length = int((len(files) - training_length)/2)
    shuffled_set = random.sample(files, len(files))
    training_set = shuffled_set[0:training_length]
    print(f"Train dataset size: {len(training_set)}")
    validation_set = shuffled_set[training_length:training_length+validation_length]
    print(f"Validation dataset size: {len(validation_set)}")
    testing_set = shuffled_set[training_length+validation_length:]
    print(f"Test dataset size: {len(testing_set)}")

    for filename in training_set:
        this_file = os.path.join(SOURCE,filename)
        destination = os.path.join(TRAINING, filename)
        copyfile(this_file, destination)
    print(f"Train dataset created with size: {len(os.listdir(TRAINING))}")

    for filename in validation_set:
        this_file = os.path.join(SOURCE,filename)
        destination = os.path.join(VALIDATION, filename)
        copyfile(this_file, destination)
    print(f"Validation dataset created with size: {len(os.listdir(VALIDATION))}")

    for filename in testing_set:
        this_file = os.path.join(SOURCE,filename)
        destination = os.path.join(TESTING, filename)
        copyfile(this_file, destination)
    print(f"Test dataset created with size: {len(os.listdir(TESTING))}")

def class_split(classes, split_size):
  for c in classes:
    SOURCE_DIR = os.path.join(path,c)
    TRAINING_DIR = os.path.join(path, 'split/train/', c)
    VALIDATION_DIR = os.path.join(path, 'split/val/', c)
    TESTING_DIR = os.path.join(path, 'split/test/', c)
    split_data(SOURCE_DIR, TRAINING_DIR, VALIDATION_DIR, TESTING_DIR, split_size)

#Split the data with ratio train: validation: test --> 0.7: 0.15: 0.15
