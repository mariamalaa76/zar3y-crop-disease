# Load and convert image to tensor
def load_image(path):
    img = tf.io.read_file(path)
    img = tf.image.decode_jpeg(img, channels=3)
    img = tf.image.resize(img, [224, 224])
    img = img / 255.0   # Normalize to [0,1]
    return img

augmentation = get_augmentation_pipeline()
